/*
 * Builds the page's importmap for 3rd-party libraries (three.js, lil-gui).
 * The script auto-detects its own location, so it works from index.html
 * (./vendor-importmap.js) or tools/*.html (../vendor-importmap.js) with no
 * per-page configuration.
 *
 * The local-vs-CDN decision is BAKED AT BUILD TIME into the VENDOR block
 * below (no runtime probing — the old synchronous-XHR HEAD probes blocked the
 * main thread and 404'd on the CDN-only Pages deploy). The committed default
 * is CDN, which is what the deploy and a fresh checkout serve.
 *
 *   For offline / local dev with vendored libraries:
 *       npm run importmap:local   # detects three.js/ + node_modules/ and rewrites VENDOR
 *   To revert to CDN:
 *       npm run importmap         # rewrites VENDOR back to all-CDN
 *
 * Do NOT commit a `local` VENDOR block — it would break the live deploy.
 *
 * If a page needs page-specific local imports (e.g. tool helpers), assign
 * window.__DAYDREAM_EXTRA_IMPORTS = { name: '...' } before this script.
 */
(function () {
  // === GENERATED by scripts/generate-importmap.mjs — do not edit by hand ===
  // Per-library resolution baked at build time. 'cdn' (default, deploy-safe)
  // or 'local' (offline dev with vendored dirs). Versions come from
  // package.json. Regenerate, don't hand-edit.
  const VENDOR = { three: 'cdn', lilGui: 'cdn' };
  const THREE_VERSION = '0.183.1';
  const LIL_GUI_VERSION = '0.21.0';
  // === END GENERATED ===

  // document.currentScript is null when this file is loaded as an ES module
  // (type="module") or after the script has finished executing. This builder
  // must run as a classic synchronous <script> so it can detect its own URL and
  // inject the importmap before the module graph resolves. Fail with a named
  // cause — like shared.js / slider.js — instead of an opaque
  // "Cannot read properties of null (reading 'src')".
  const selfScript = document.currentScript;
  if (!selfScript) {
    throw new Error('vendor-importmap.js: document.currentScript is null — load ' +
      'this as a classic <script> (not type="module" or a deferred/dynamic ' +
      'import); it must run synchronously to detect its own path and inject the ' +
      'importmap before modules load.');
  }
  const ROOT = new URL('.', selfScript.src);
  const EXTRA = window.__DAYDREAM_EXTRA_IMPORTS || {};

  const CDN = 'https://cdn.jsdelivr.net/npm';

  const abs = (path) => new URL(path, ROOT).href;

  const threeBase = VENDOR.three === 'local'
    ? abs('three.js/')
    : `${CDN}/three@${THREE_VERSION}/`;
  const lilGui = VENDOR.lilGui === 'local'
    ? abs('node_modules/lil-gui/dist/lil-gui.esm.min.js')
    : `${CDN}/lil-gui@${LIL_GUI_VERSION}/dist/lil-gui.esm.min.js`;

  const imports = Object.assign({
    'three': `${threeBase}build/three.module.js`,
    'three/addons/': `${threeBase}examples/jsm/`,
    'lil-gui': lilGui,
    'gui': abs('gui.js'),
  }, EXTRA);

  const s = document.createElement('script');
  s.type = 'importmap';
  s.textContent = JSON.stringify({ imports });
  document.head.appendChild(s);
})();
